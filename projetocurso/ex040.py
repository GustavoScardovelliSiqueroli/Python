nota1 = float(input('Digete sua nota1:.. '))
nota2 = float(input('Digite sua nota2:.. '))
media = (nota1 + nota2)/2
if media < 5:
    print('Você está reprovado seu imbecil...')
elif 5 <= media < 7:
    print('Você está de recuperação, não fez nem o basico impacaz!')
else:
    print('Você está aprovado nessa merda, fez o básico...')

